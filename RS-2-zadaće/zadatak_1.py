kvadriraj = lambda x : x ** 2

# Zbroji pa kvadriraj
zbroji_pa_kvadriraj = lambda a,b : (a + b) ** 2

# Kvadriraj duljinu niza
kvadriraj_duljinu = lambda niz : len(niz) ** 2

# Pomno≈æi vrijednost s 5 pa potenciraj na x
